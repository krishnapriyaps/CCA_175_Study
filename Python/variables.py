# Two integers
a = 1
b = 2

# Addition of two integers return integer
c = a + b
print(c)
print(type(c))

# Division of two integers return integer
d = a / b
print(d)
print(type(d))


# Division of 1 integers and float return float
e = a / float(b)
print(e)
print("Op 1 integers and float :", type(e))
